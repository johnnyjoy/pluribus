package pgtextsearch

import (
	"context"
	"database/sql"
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"time"

	"control-plane/internal/migrate"
	sqlmigrations "control-plane/migrations"

)

// EvalOptions configures the one-shot eval pipeline.
type EvalOptions struct {
	DSN              string
	ProjectionTable  string
	ReplaceSeed      bool
	SkipMigrate      bool
	ArtifactDir      string // e.g. artifacts/pg-textsearch
	MarkdownPath     string // e.g. docs/experiments/pg-textsearch-eval-latest.md
	QueryLimit       int
}

// EvalReport is machine-readable summary output.
type EvalReport struct {
	StartedAtUTC       string              `json:"started_at_utc"`
	FinishedAtUTC      string              `json:"finished_at_utc"`
	DSNRedacted        string              `json:"dsn_redacted"`
	SeededRows         int                 `json:"seeded_rows,omitempty"`
	ProjectionRows     int64               `json:"projection_rows"`
	IndexBuildMS       float64             `json:"index_build_ms,omitempty"`
	VerifyOK           bool                `json:"verify_ok"`
	VerifyMessage      string              `json:"verify_message"`
	QuerySuite         []SuiteResult       `json:"query_suite"`
	PlausibleRatio     float64             `json:"plausible_ratio"`
	Recommendation     string              `json:"recommendation"`
	Errors             []string            `json:"errors,omitempty"`
}

// RunEval executes migrate → extension → seed → reindex → verify → query suite → artifacts.
func RunEval(ctx context.Context, db *sql.DB, opt EvalOptions) (*EvalReport, error) {
	rep := &EvalReport{
		StartedAtUTC: time.Now().UTC().Format(time.RFC3339),
		DSNRedacted:  redactDSN(opt.DSN),
	}
	var errs []string

	if !opt.SkipMigrate {
		if err := migrate.Apply(ctx, db, sqlmigrations.Files, func(string, ...any) {}); err != nil {
			return nil, fmt.Errorf("migrate: %w", err)
		}
	}

	if err := EnsureExtension(ctx, db); err != nil {
		return nil, fmt.Errorf("pg_textsearch extension: %w (is shared_preload_libraries=pg_textsearch set?)", err)
	}

	n, err := Seed(ctx, db, opt.ReplaceSeed)
	if err != nil {
		return nil, fmt.Errorf("seed: %w", err)
	}
	rep.SeededRows = n

	t0 := time.Now()
	if err := Reindex(ctx, db); err != nil {
		return nil, fmt.Errorf("reindex: %w", err)
	}
	rep.IndexBuildMS = float64(time.Since(t0).Milliseconds())

	var nrow int64
	_ = db.QueryRowContext(ctx, `SELECT COUNT(*) FROM lexical_memory_projection`).Scan(&nrow)
	rep.ProjectionRows = nrow

	v, err := Verify(ctx, db)
	if err != nil {
		return nil, err
	}
	rep.VerifyOK = v.OK
	rep.VerifyMessage = v.Message

	limit := opt.QueryLimit
	if limit <= 0 {
		limit = 8
	}
	pt := opt.ProjectionTable
	if pt == "" {
		pt = DefaultProjectionTable
	}
	suite, err := RunQuerySuite(ctx, db, pt, limit)
	if err != nil {
		return nil, err
	}
	rep.QuerySuite = suite

	plausible := 0
	for _, q := range suite {
		if q.Plausible {
			plausible++
		}
	}
	if len(suite) > 0 {
		rep.PlausibleRatio = float64(plausible) / float64(len(suite))
	}
	if err := FailIfSuiteEmpty(suite); err != nil {
		errs = append(errs, err.Error())
	}
	rep.Errors = errs

	rep.Recommendation = buildRecommendation(rep)
	rep.FinishedAtUTC = time.Now().UTC().Format(time.RFC3339)

	if opt.ArtifactDir != "" {
		_ = os.MkdirAll(opt.ArtifactDir, 0o755)
		b, _ := json.MarshalIndent(rep, "", "  ")
		_ = os.WriteFile(filepath.Join(opt.ArtifactDir, "eval.json"), b, 0o644)
	}
	if opt.MarkdownPath != "" {
		md := renderMarkdown(rep)
		_ = os.MkdirAll(filepath.Dir(opt.MarkdownPath), 0o755)
		if err := os.WriteFile(opt.MarkdownPath, []byte(md), 0o644); err != nil {
			return rep, err
		}
	}
	return rep, nil
}

func buildRecommendation(r *EvalReport) string {
	if len(r.Errors) > 0 {
		return "fix errors above before relying on lexical path"
	}
	if !r.VerifyOK {
		return "verify failed: projection does not match canonical memories"
	}
	if r.PlausibleRatio < 0.3 {
		return "weak: most sample queries did not surface plausible top results — tune doc_text or queries"
	}
	if r.PlausibleRatio >= 0.5 && r.VerifyOK {
		return "promising: BM25 path is operational and sample queries look plausible — proceed to hybrid experiments"
	}
	return "mixed: pipeline works; review per-query results before broader investment"
}

func redactDSN(dsn string) string {
	// Hide password in postgres://user:pass@host/...
	if len(dsn) < 20 {
		return "(short)"
	}
	return "postgres://****@" + afterAt(dsn)
}

func afterAt(s string) string {
	for i := range s {
		if s[i] == '@' {
			return s[i+1:]
		}
	}
	return s
}

func renderMarkdown(r *EvalReport) string {
	var b string
	b += "# pg_textsearch eval (latest)\n\n"
	b += "Auto-generated by `make pg-textsearch-eval`. **Do not hand-edit** — re-run the target to refresh.\n\n"
	b += "## Summary\n\n"
	b += fmt.Sprintf("- **Verify:** %v — %s\n", r.VerifyOK, r.VerifyMessage)
	b += fmt.Sprintf("- **Projection rows:** %d\n", r.ProjectionRows)
	b += fmt.Sprintf("- **Plausible query ratio:** %.2f\n", r.PlausibleRatio)
	b += fmt.Sprintf("- **Index/reindex wall time (ms):** %.1f\n", r.IndexBuildMS)
	b += fmt.Sprintf("- **Recommendation:** %s\n\n", r.Recommendation)
	if len(r.Errors) > 0 {
		b += "## Errors\n\n"
		for _, e := range r.Errors {
			b += fmt.Sprintf("- %s\n", e)
		}
		b += "\n"
	}
	b += "## Query suite\n\n"
	b += "| Category | Query | Latency (ms) | Plausible | Top statement (truncated) |\n"
	b += "|----------|-------|--------------|-----------|---------------------------|\n"
	for _, q := range r.QuerySuite {
		top := ""
		if len(q.TopStatements) > 0 {
			top = truncate(q.TopStatements[0], 80)
		}
		b += fmt.Sprintf("| %s | %s | %.2f | %v | %s |\n", q.Category, q.Query, q.LatencyMS, q.Plausible, top)
	}
	b += "\n## Machine-readable\n\n"
	b += "`artifacts/pg-textsearch/eval.json`\n"
	return b
}

func truncate(s string, n int) string {
	if len(s) <= n {
		return s
	}
	return s[:n] + "…"
}
